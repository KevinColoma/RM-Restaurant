let currentLang = localStorage.getItem('rms-lang') || 'en';

function t(key) {
  return translations[currentLang] && translations[currentLang][key] !== undefined
    ? translations[currentLang][key]
    : (translations.en[key] || key);
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = t(key);
  });
  document.querySelectorAll('[data-i18n-value]').forEach(function(el) {
    const key = el.getAttribute('data-i18n-value');
    el.value = t(key);
  });
  document.documentElement.lang = currentLang === 'es' ? 'es' : 'en';
}

function setLanguage(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem('rms-lang', lang);
  applyTranslations();
  const flagImg = document.querySelector('.flag-nav .nav-link img');
  if (flagImg) {
    const flags = { en: 'us', es: 'es' };
    flagImg.src = 'assets/img/flags/' + (flags[lang] || 'us') + '.png';
  }
}
